// "New project" scaffold: turns the generated infra/runtime files into a complete,
// runnable Next.js 16 + SST v4 app (yarn install && sst dev) plus an AGENTS.md so
// Claude / any dev can open the export and finish it.

const sortObj = (o: Record<string, string>): Record<string, string> =>
  Object.fromEntries(Object.entries(o).sort(([a], [b]) => a.localeCompare(b)));

function packageJson(
  bp: Blueprint,
  deps: Record<string, string>,
  devDeps: Record<string, string>,
): string {
  // Verified major ranges (npm registry, 2026-06-10).
  const dependencies = sortObj({
    next: '^16.0.0',
    react: '^19.0.0',
    'react-dom': '^19.0.0',
    ...deps,
  });
  const pkg = {
    name: kebabCase(bp.app.name) || 'sst-app',
    version: '0.1.0',
    private: true,
    type: 'module',
    // No lint script: the scaffold ships no eslint config/dependency.
    scripts: {
      dev: 'next dev',
      build: 'next build',
      start: 'next start',
      'sst:dev': 'sst dev',
      deploy: 'sst deploy',
      remove: 'sst remove',
    },
    dependencies,
    devDependencies: sortObj({
      '@types/node': '^25.0.0',
      '@types/react': '^19.0.0',
      '@types/react-dom': '^19.0.0',
      typescript: '^6.0.0',
      ...devDeps,
    }),
  };
  return `${JSON.stringify(pkg, null, 2)}\n`;
}

