import type { Blueprint } from '@/lib/core/blueprint/types';
import type { GeneratedFile } from '@/lib/core/codegen/types';
import { kebabCase } from '@/lib/core/codegen/strings';
import { AWS_CATALOG } from '../catalog';
import { collectAwsEnv } from '../env';

// "New project" scaffold: turns the generated infra/runtime files into a complete,
// runnable Next.js 16 + SST v4 app (yarn install && sst dev) plus an AGENTS.md so
// Claude / any dev can open the export and finish it.

const sortObj = (o: Record<string, string>): Record<string, string> =>
  Object.fromEntries(Object.entries(o).sort(([a], [b]) => a.localeCompare(b)));

function packageJson(bp: Blueprint, deps: Record<string, string>): string {
  const dependencies = sortObj({
    next: 'latest',
    react: 'latest',
    'react-dom': 'latest',
    ...deps,
  });
  const pkg = {
    name: kebabCase(bp.app.name) || 'sst-app',
    version: '0.1.0',
    private: true,
    type: 'module',
    scripts: {
      dev: 'next dev',
      build: 'next build',
      start: 'next start',
      lint: 'eslint',
      'sst:dev': 'sst dev',
      deploy: 'sst deploy',
      remove: 'sst remove',
    },
    dependencies,
    devDependencies: sortObj({
      '@types/node': 'latest',
      '@types/react': 'latest',
      '@types/react-dom': 'latest',
      typescript: 'latest',
    }),
  };
  return `${JSON.stringify(pkg, null, 2)}\n`;
}

