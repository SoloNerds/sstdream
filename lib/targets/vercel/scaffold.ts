// "New project" scaffold for the Vercel lane: turns the generated integration
// files into a complete, runnable Next.js app you deploy on Vercel. Verified
// against docs/vercel-target.md §0-§1: deploying Next.js to Vercel is zero-config
// (no vercel.json needed for a standard app), Git integration is the recommended
// path, and there is no SST/AWS infra. Mirrors the AWS scaffold so both lanes
// export a real project + an AGENTS.md, not loose fragments.

const sortObj = (o: Record<string, string>): Record<string, string> =>
  Object.fromEntries(Object.entries(o).sort(([a], [b]) => a.localeCompare(b)));

function packageJson(bp: Blueprint, deps: Record<string, string>): string {
  // Verified major ranges (npm registry); service deps come from the generator.
  const dependencies = sortObj({
    next: '^16.0.0',
    react: '^19.0.0',
    'react-dom': '^19.0.0',
    ...deps,
  });
  const pkg = {
    name: kebabCase(bp.app.name) || 'vercel-app',
    version: '0.1.0',
    private: true,
    scripts: {
      dev: 'next dev',
      build: 'next build',
      start: 'next start',
      // Git integration is the recommended path; this is the manual CLI deploy.
      deploy: 'vercel --prod',
    },
    dependencies,
    devDependencies: sortObj({
      '@types/node': '^25.0.0',
      '@types/react': '^19.0.0',
      '@types/react-dom': '^19.0.0',
      typescript: '^6.0.0',
    }),
  };
  return `${JSON.stringify(pkg, null, 2)}\n`;
}

